// Checking marks and giving result

let marks = 85;

if (marks >= 90) {
    console.log("Grade: O - Outstanding!");
} else if (marks >= 80) {
    console.log("Grade: A - Very Good!");
} else if (marks >= 70) {
    console.log("Grade: B - Good!");
} else if (marks >= 60) {
    console.log("Grade: C - Average!");
} else {
    console.log("Grade: Fail - Need Improvement");
}
