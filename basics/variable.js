// var - we can change it
var a = 10;
console.log("var a:", a);  // shows 10

a = 20;
console.log("changed var a:", a);  // shows 20

// let - also we can change it
let b = 30;
console.log("let b:", b);  // shows 30

b = 40;
console.log("changed let b:", b);  // shows 40

// const - we cannot change it
const c = 50;
console.log("const c:", c);  // shows 50

// c = 60; // This line will give error if we try to change const
